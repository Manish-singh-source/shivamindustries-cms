    const searchResults = document.getElementById('shivamSearchResults');

    // Toggle search box
    searchBtn.addEventListener('click', function(e) {
        e.stopPropagation();
        searchBox.classList.toggle('active');
        if (searchBox.classList.contains('active')) {
            searchInput.focus();
        }
    });

    // Close search box
    searchClose.addEventListener('click', function() {
        searchBox.classList.remove('active');
        searchInput.value = '';
        searchResults.innerHTML = '';
        searchResults.style.display = 'none';
    });

    // Search functionality
    searchInput.addEventListener('input', function() {
        const query = this.value.toLowerCase().trim();

        if (query.length < 1) {
            searchResults.innerHTML = '';
            searchResults.style.display = 'none';
            return;
        }

        const filtered = products.filter(p => p.name.toLowerCase().includes(query));

        if (filtered.length > 0) {
            searchResults.innerHTML = filtered.map(p =>
                `<a href="${p.url}" class="shivam-search-result-item">
                    <i class="fas fa-flask"></i> ${p.name}
                </a>`
            ).join('');
            searchResults.style.display = 'block';
        } else {
            searchResults.innerHTML = '<div class="shivam-search-no-result">No products found</div>';
            searchResults.style.display = 'block';
        }
    });

    // Close on click outside
    document.addEventListener('click', function(e) {
        if (!searchBox.contains(e.target) && !searchBtn.contains(e.target)) {
            searchBox.classList.remove('active');
            searchResults.style.display = 'none';
        }
    });

